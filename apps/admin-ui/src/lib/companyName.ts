/**
 * Сокращение организационно-правовых форм для отображения в UI.
 * ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ → ООО и т.д.
 */

// \b не работает с кириллицей в JS — используем явные фразы без word boundary
const LEGAL_FORM_MAP: Array<{ full: RegExp; short: string }> = [
  { full: /ОБЩЕСТВО\s+С\s+ОГРАНИЧЕННОЙ\s+ОТВЕТСТВЕННОСТЬЮ/gi, short: 'ООО' },
  { full: /ОТКРЫТОЕ\s+АКЦИОНЕРНОЕ\s+ОБЩЕСТВО/gi, short: 'ОАО' },
  { full: /ЗАКРЫТОЕ\s+АКЦИОНЕРНОЕ\s+ОБЩЕСТВО/gi, short: 'ЗАО' },
  { full: /ПУБЛИЧНОЕ\s+АКЦИОНЕРНОЕ\s+ОБЩЕСТВО/gi, short: 'ПАО' },
  { full: /НЕПУБЛИЧНОЕ\s+АКЦИОНЕРНОЕ\s+ОБЩЕСТВО/gi, short: 'НАО' },
  { full: /ИНДИВИДУАЛЬНЫЙ\s+ПРЕДПРИНИМАТЕЛЬ/gi, short: 'ИП' },
  { full: /КРЕСТЬЯНСКОЕ\s+\(ФЕРМЕРСКОЕ\)\s+ХОЗЯЙСТВО/gi, short: 'КФХ' },
  { full: /АВТОНОМНАЯ\s+НЕКОММЕРЧЕСКАЯ\s+ОРГАНИЗАЦИЯ/gi, short: 'АНО' },
  { full: /ОБЩЕСТВЕННАЯ\s+ОРГАНИЗАЦИЯ/gi, short: 'ОО' },
  { full: /ПОТРЕБИТЕЛЬСКОЕ\s+ОБЩЕСТВО/gi, short: 'ПО' },
  { full: /ЖИЛИЩНО-СТРОИТЕЛЬНЫЙ\s+КООПЕРАТИВ/gi, short: 'ЖСК' },
  { full: /ЖИЛИЩНЫЙ\s+НАКОПИТЕЛЬНЫЙ\s+КООПЕРАТИВ/gi, short: 'ЖНК' },
  { full: /СЕЛЬСКОХОЗЯЙСТВЕННЫЙ\s+ПРОИЗВОДСТВЕННЫЙ\s+КООПЕРАТИВ/gi, short: 'СПК' },
  { full: /ГОСУДАРСТВЕННОЕ\s+УНИТАРНОЕ\s+ПРЕДПРИЯТИЕ/gi, short: 'ГУП' },
  { full: /МУНИЦИПАЛЬНОЕ\s+УНИТАРНОЕ\s+ПРЕДПРИЯТИЕ/gi, short: 'МУП' },
  { full: /ФЕДЕРАЛЬНОЕ\s+ГОСУДАРСТВЕННОЕ\s+УНИТАРНОЕ\s+ПРЕДПРИЯТИЕ/gi, short: 'ФГУП' },
  { full: /ФОНД/gi, short: 'Фонд' },
];

/**
 * Сокращает ОПФ в названии компании для компактного отображения.
 * @example abbreviateLegalForm('ОБЩЕСТВО С ОГРАНИЧЕННОЙ ОТВЕТСТВЕННОСТЬЮ "РОГА И КОПЫТА"') → 'ООО "РОГА И КОПЫТА"'
 */
export function abbreviateLegalForm(name: string | null | undefined): string {
  if (!name || typeof name !== 'string') return '';
  let result = name.trim();
  for (const { full, short } of LEGAL_FORM_MAP) {
    result = result.replace(full, short);
  }
  return result.trim();
}
